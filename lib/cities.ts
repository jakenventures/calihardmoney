export function getCityBySlug(slug: string): City | undefined {
  return CITIES.find(city => city.slug === slug);
}

export function getNearbyCities(currentSlug: string, count: number = 5): City[] {
  const currentIndex = CITIES.findIndex(city => city.slug === currentSlug);
  if (currentIndex === -1) return [];
  
  const nearby: City[] = [];
  
  for (let i = 1; i <= count && nearby.length < count; i++) {
    if (currentIndex + i < CITIES.length) {
      nearby.push(CITIES[currentIndex + i]);
    }
    if (nearby.length < count && currentIndex - i >= 0) {
      nearby.push(CITIES[currentIndex - i]);
    }
  }
  
  return nearby;
}